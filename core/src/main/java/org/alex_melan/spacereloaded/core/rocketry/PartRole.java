package org.alex_melan.spacereloaded.core.rocketry;

/** Роль детали в структуре ракеты. */
public enum PartRole {
    /** Силовой/обшивочный блок: только масса. */
    HULL,
    /** Двигатель: тяга, удельный импульс, тип топлива. */
    ENGINE,
    /** Бак: ёмкость по массе топлива. */
    TANK,
    /** Командный модуль: точка сборки и управления. */
    COMMAND,
    /** Кресло пассажира/пилота. */
    SEAT,
    /** Гиродин: компенсирующий момент для стабилизации. */
    GYRO,
    /** Декоративный блок из белого списка: масса по умолчанию. */
    DECORATIVE
}
