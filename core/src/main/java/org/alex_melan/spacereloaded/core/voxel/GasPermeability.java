package org.alex_melan.spacereloaded.core.voxel;

/**
 * Проницаемость ячейки для газа с точки зрения проверки герметичности.
 */
public enum GasPermeability {
    /** Газ проходит: воздух, открытая дверь, решётка. */
    OPEN,
    /** Газ не проходит: герметичный блок, закрытая дверь. */
    BLOCKED,
    /**
     * Заведомо «наружа»: открытый космос, небо. Контакт заполнения с такой
     * ячейкой — доказанная утечка (LEAK), в отличие от простого выхода за
     * радиус поиска (UNBOUNDED).
     */
    VACUUM,
    /** За пределами снимка/мира; трактуется как стена (как границы мира в исходном плагине). */
    OUT_OF_BOUNDS
}
