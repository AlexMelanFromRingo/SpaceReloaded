package org.alex_melan.spacereloaded.core.sealing;

/**
 * Итог проверки замкнутости объёма.
 */
public enum SealingStatus {
    /** Объём замкнут в пределах радиуса — герметично. */
    SEALED,
    /** Заполнение коснулось вакуума — доказанная утечка. */
    LEAK,
    /**
     * Заполнение вышло за радиус поиска — объём не замкнут в пределах лимита.
     * Для геймплея это «не герметично», но в диагностике отличимо от LEAK.
     */
    UNBOUNDED,
    /** Стартовая ячейка непроницаема — проверка не имеет смысла. */
    INVALID_ORIGIN
}
